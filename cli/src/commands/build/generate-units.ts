import { resolve } from 'node:path';
import { writeFileSync } from 'node:fs';
import { generateDeploymentPlan } from '@/codegen/deploy/index.js';
import type { ParsedEntity } from '@/codegen/parser/types/parsed-entity.js';

/** Build a map of entityType → path IDs from the parsed entity tree. */
function buildTypeToPathIds(entity: ParsedEntity, prefix: string): Map<string, string[]> {
  const map = new Map<string, string[]>();
  const ids = map.get(entity.type) ?? [];
  ids.push(prefix);
  map.set(entity.type, ids);

  for (const comp of entity.components) {
    if (!comp.entity) continue;
    const childPath = `${prefix}.${comp.propertyName}`;
    const childMap = buildTypeToPathIds(comp.entity, childPath);
    for (const [type, paths] of childMap) {
      const existing = map.get(type) ?? [];
      existing.push(...paths);
      map.set(type, existing);
    }
  }
  return map;
}

/** Generate per-unit entrypoints + _all.ts from deployment plan. */
export function generateUnits(entities: ParsedEntity[], generatedDir: string, dev: boolean, rootEntity?: ParsedEntity) {
  if (entities.length === 0) return;

  const plan = generateDeploymentPlan(entities);
  const entityMap = new Map(entities.map(e => [e.type, e]));

  // Build parent map
  const parentOf = new Map<string, string>();
  for (const entity of entities) {
    for (const comp of entity.components) {
      parentOf.set(comp.entityType, entity.type);
    }
  }

  if (plan.units.length <= 1) return;

  // Build type → path IDs from entity tree (for endIds calculation)
  const typeToPathIds = rootEntity ? buildTypeToPathIds(rootEntity, rootEntity.type) : new Map<string, string[]>();

  // Collect ALL unique pubsub adapters used across the entire tree
  const allPubsubs = new Set<string>(['InProcessBusAdapter']);
  for (const e of entities) {
    if (e.infra.pubsub) allPubsubs.add(e.infra.pubsub);
  }

  const allUnitFiles: string[] = [];

  for (const unit of plan.units) {
    const unitEntities = unit.entities.map(t => entityMap.get(t)!).filter(Boolean);

    const unitTypeSet = new Set(unit.entities);
    const unitRoot = unitEntities.find(e => {
      const parent = parentOf.get(e.type);
      return !parent || !unitTypeSet.has(parent);
    });
    if (!unitRoot) continue;

    // Find ALL startIds — path IDs of this unit's root in the entity tree
    // (there may be multiple instances of the same entity type, e.g. teamA and teamB)
    const startIds = typeToPathIds.get(unitRoot.type) ?? [unitRoot.type];

    // Find endIds — path IDs of components that belong to OTHER units
    const endIds: string[] = [];
    for (const entity of unitEntities) {
      for (const comp of entity.components) {
        if (!unitTypeSet.has(comp.entityType)) {
          const compPathIds = typeToPathIds.get(comp.entityType) ?? [];
          endIds.push(...compPathIds);
        }
      }
    }

    // Build import lines
    const importLines: string[] = [];
    for (const entity of unitEntities) {
      const srcIdx = entity.sourceFile.indexOf('/src/');
      const relPath = srcIdx >= 0 ? entity.sourceFile.slice(srcIdx + 5) : entity.sourceFile;
      const importPath = `./${relPath.replace(/\.ts$/, '')}.js`;
      importLines.push(`import { ${entity.className} } from '${importPath}';`);
    }

    const sdkImports = ['Runner', ...allPubsubs];
    if (dev) sdkImports.push('DevLogAdapter');

    // Generate one unit file per instance path
    for (let i = 0; i < startIds.length; i++) {
      const sid = startIds[i];
      const unitName = startIds.length > 1 ? `${unit.name}-${i}` : unit.name;
      allUnitFiles.push(unitName);

      // Scope endIds to this instance path
      const scopedEndIds = endIds.filter(eid => eid.startsWith(sid + '.'));

      const bootCall = scopedEndIds.length > 0
        ? `await runner.bootRange(${unitRoot.className} as any, ${JSON.stringify(sid)}, ${JSON.stringify(scopedEndIds)});`
        : `await runner.bootRange(${unitRoot.className} as any, ${JSON.stringify(sid)});`;

      const unitLines = [
        `// AUTO-GENERATED by interactkit build — do not edit`,
        `// Deployment unit: ${unitName}`,
        `// Entities: ${unit.entities.join(', ')}`,
        `// Instance: ${sid}`,
        `// Scalable: ${unit.scalable}`,
        `// Bus: ${unit.busAdapter}`,
        "import 'dotenv/config';",
        "import 'reflect-metadata';",
        `import { ${sdkImports.join(', ')} } from '@interactkit/sdk';`,
        "import { entityTree } from './entity-tree.js';",
        ...importLines,
        '',
        'const runner = new Runner(entityTree, {',
        `  pubsubs: [${[...allPubsubs].map(ps => `{ name: "${ps}", adapter: new ${ps}() }`).join(', ')}],`,
        '  databases: [],',
        dev ? '  loggers: [{ name: "DevLogAdapter", adapter: new DevLogAdapter() }],' : '  loggers: [],',
        '});',
        '',
        `const { root, shutdown } = ${bootCall}`,
        `console.log(\`▸ ${unitName}: booted from \${root.id}\`);`,
        '',
        "process.on('SIGINT', async () => {",
        '  await shutdown();',
        '  process.exit(0);',
        '});',
        '',
      ];
      writeFileSync(resolve(generatedDir, `_${unitName}.ts`), unitLines.join('\n'), 'utf-8');
    }
  }
  console.log(`  ${allUnitFiles.length} unit entrypoints generated`);

  // Generate _all.ts
  const allLines = [
    '// AUTO-GENERATED by interactkit build — do not edit',
    '// Runs all deployment units + hook server in a single process.',
    ...allUnitFiles.map(u => `import './_${u}.js';`),
    "import './_hooks.js';",
    '',
  ];
  writeFileSync(resolve(generatedDir, '_all.ts'), allLines.join('\n'), 'utf-8');
}
