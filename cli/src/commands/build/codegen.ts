import { resolve } from 'node:path';
import { mkdirSync, writeFileSync } from 'node:fs';
import { Project } from 'ts-morph';
import { extractEntities } from '@/codegen/parser/index.js';
import { emit } from '@/codegen/emit/index.js';
import { generateDeploymentPlan } from '@/codegen/deploy/index.js';
import type { ParsedEntity } from '@/codegen/parser/types/parsed-entity.js';

/** Run entity extraction + emit type-registry.ts + entity-tree.ts + deployment.json */
export function runCodegen(projectPath: string, generatedDir: string, rootClassName?: string): ParsedEntity[] {
  console.log('▸ codegen');
  const project = new Project({ tsConfigFilePath: projectPath });
  const entities = extractEntities(project);

  mkdirSync(generatedDir, { recursive: true });

  if (entities.length === 0) {
    console.log('  No @Entity classes found.');
    writeFileSync(resolve(generatedDir, 'type-registry.ts'), 'export const Registry = { entities: {} } as const;\n');
    writeFileSync(resolve(generatedDir, 'entity-tree.ts'), 'export const entityTree = null;\n');
  } else {
    const code = emit(entities);
    writeFileSync(resolve(generatedDir, 'type-registry.ts'), code, 'utf-8');

    // Generate entity-tree.ts — find the root entity (by className or first with no parent)
    const root = rootClassName
      ? entities.find(e => e.className === rootClassName)
      : entities.find(e => !entities.some(p => p.components.some(c => c.entityType === e.type)));
    if (!root) {
      console.error(`  Root entity "${rootClassName ?? '(auto)'}" not found`);
      process.exit(1);
    }
    writeFileSync(resolve(generatedDir, 'entity-tree.ts'), emitEntityTree(root), 'utf-8');

    const methodCount = entities.reduce((sum, e) => sum + e.methods.length, 0);
    const hookCount = entities.reduce((sum, e) => sum + e.hooks.length, 0);
    console.log(`  ${entities.length} entities, ${methodCount} methods, ${hookCount} hooks`);

    const plan = generateDeploymentPlan(entities);
    writeFileSync(resolve(generatedDir, 'deployment.json'), JSON.stringify(plan, null, 2), 'utf-8');
    console.log(`  ${plan.units.length} deployment units, ${plan.connections.length} cross-unit connections`);
  }

  return entities;
}

/** Emit a ParsedEntity tree as a TypeScript EntityNode constant with pre-calculated IDs. */
function emitEntityTree(entity: ParsedEntity): string {
  return [
    '// AUTO-GENERATED by @interactkit/cli codegen — do not edit',
    '',
    `export const entityTree = ${emitNode(entity, entity.type, 0)};`,
    '',
  ].join('\n');
}

function J(v: unknown) { return JSON.stringify(v); }
function U(v: string | undefined) { return v ? J(v) : 'undefined'; }

function emitNode(entity: ParsedEntity, nodeId: string, indent: number): string {
  const pad = '  '.repeat(indent);
  const n = '  '.repeat(indent + 1);

  const childId = (prop: string) => `${nodeId}.${prop}`;

  const comps = entity.components.map(c => {
    const cid = childId(c.propertyName);
    const child = c.entity ? emitNode(c.entity, cid, indent + 2) : 'undefined';
    return `${n}  { id: ${J(cid)}, propertyName: ${J(c.propertyName)}, entityType: ${J(c.entityType)}, entity: ${child} }`;
  });

  return [
    `{`,
    `${n}id: ${J(nodeId)},`,
    `${n}type: ${J(entity.type)},`,
    `${n}className: ${J(entity.className)},`,
    `${n}infra: { pubsub: ${U(entity.infra.pubsub)}, database: ${U(entity.infra.database)}, logger: ${U(entity.infra.logger)} },`,
    `${n}state: [${entity.state.map(s => `{ name: ${J(s.name)}, id: ${J(childId(s.name))} }`).join(', ')}],`,
    `${n}refs: [${entity.refs.map(r => `{ propertyName: ${J(r.propertyName)}, targetEntityType: ${J(r.targetEntityType)}, id: ${J(childId(r.propertyName))} }`).join(', ')}],`,
    `${n}components: [`,
    comps.join(',\n'),
    `${n}],`,
    `${n}streams: [${entity.streams.map(s => `{ propertyName: ${J(s.propertyName)}, id: ${J(childId(s.propertyName))} }`).join(', ')}],`,
    `${n}methods: [${entity.methods.map(m => `{ methodName: ${J(m.methodName)}, eventName: ${J(m.eventName)}, id: ${J(childId(m.methodName))} }`).join(', ')}],`,
    `${n}hooks: [${entity.hooks.map(h => `{ methodName: ${J(h.methodName)}, hookTypeName: ${J(h.hookTypeName)}, runnerExport: ${U(h.runnerExport)}, inProcess: ${h.inProcess}, id: ${J(childId(h.methodName))} }`).join(', ')}],`,
    `${pad}}`,
  ].join('\n');
}
