import { resolve } from 'node:path';
import { writeFileSync } from 'node:fs';
import type { ParsedEntity } from '@/codegen/parser/types/parsed-entity.js';

/**
 * Generate _hooks.ts — a standalone hook server process.
 *
 * Groups hooks by runner namespace (e.g. all HttpRequest hooks share one init).
 * For each group: init once, then register each hook's path/config separately.
 */
export function generateHooks(entities: ParsedEntity[], generatedDir: string) {
  const remoteHooks: Array<{
    entityType: string;
    methodName: string;
    runnerExport: string;
    sourcePackage?: string;
  }> = [];

  for (const entity of entities) {
    for (const hook of entity.hooks) {
      if (!hook.inProcess && hook.runnerExport) {
        remoteHooks.push({
          entityType: entity.type,
          methodName: hook.methodName,
          runnerExport: hook.runnerExport,
          sourcePackage: hook.sourcePackage,
        });
      }
    }
  }

  if (remoteHooks.length === 0) {
    writeFileSync(resolve(generatedDir, '_hooks.ts'), '// No remote hooks — all hooks run in-process via HookWrapper.\n');
    return;
  }

  // Collect unique namespace → package imports
  const nsImports = new Map<string, string>();
  for (const hook of remoteHooks) {
    const ns = hook.runnerExport.split('.')[0];
    if (hook.sourcePackage && !nsImports.has(ns)) {
      nsImports.set(ns, hook.sourcePackage);
    }
  }

  // Group hooks by runner namespace — one init per group
  const groups = new Map<string, typeof remoteHooks>();
  for (const hook of remoteHooks) {
    const ns = hook.runnerExport.split('.')[0];
    if (!groups.has(ns)) groups.set(ns, []);
    groups.get(ns)!.push(hook);
  }

  const lines = [
    '// AUTO-GENERATED by interactkit build — do not edit',
    `// Hook server: ${remoteHooks.length} remote hook(s) in ${groups.size} runner group(s).`,
    "import type { InteractKitConfig } from '@interactkit/sdk';",
    "import _config from './interactkit.config.js';",
    "const config = _config as InteractKitConfig;",
  ];

  for (const [ns, pkg] of nsImports) {
    lines.push(`import { ${ns} } from '${pkg}';`);
  }

  lines.push('');
  lines.push('const pubsub = config.pubsub!;');
  lines.push('const hooksConfig = (config.hooks ?? {}) as Record<string, unknown>;');
  lines.push('');
  lines.push('(async () => {');

  for (const [ns, hooks] of groups) {
    lines.push(`  // ─── ${ns} (${hooks.length} hook${hooks.length > 1 ? 's' : ''}) ───`);
    lines.push(`  {`);
    // Init shared runner once (e.g. HTTP server on a port)
    lines.push(`    const _init = ${hooks[0].runnerExport};`);
    lines.push(`    const _shared = new _init.runnerClass();`);
    lines.push(`    await _shared.init({ ..._init.initConfig, ..._init.config, ...hooksConfig });`);
    lines.push(``);

    for (const hook of hooks) {
      const hookKey = `${hook.entityType}.${hook.methodName}`;
      const registerChannel = `hook-register:${hookKey}`;

      lines.push(`    // ${hookKey}`);
      lines.push(`    await pubsub.consume('${registerChannel}', (msg: unknown) => {`);
      lines.push(`      const { entityId, dataChannel, config: runConfig } = msg as any;`);
      lines.push(`      _shared.register(`);
      lines.push(`        async (data: unknown) => { await pubsub.publish(dataChannel, data); },`);
      lines.push(`        runConfig,`);
      lines.push(`      );`);
      lines.push(`      console.log(\`  ▸ registered \${entityId} on ${hookKey}\`);`);
      lines.push(`    });`);
    }

    lines.push(``);
    lines.push(`    console.log('▸ ${ns}: ${hooks.length} hook${hooks.length > 1 ? 's' : ''} ready');`);
    lines.push(`  }`);
  }

  lines.push('})();');
  lines.push('');

  writeFileSync(resolve(generatedDir, '_hooks.ts'), lines.join('\n'), 'utf-8');
}
