import { resolve } from 'node:path';
import { writeFileSync } from 'node:fs';
import type { ParsedEntity } from '@/codegen/parser/types/parsed-entity.js';

/**
 * Generate _hooks.ts — a hook server that starts remote hook runners
 * and publishes their emit data to pubsub channels.
 *
 * Remote hooks (inProcess: false) run their runner in a separate process.
 * When the runner fires emit(data), it's published to the entity's pubsub channel.
 * The entity process listens via HookWrapper.listenFromRemote().
 */
export function generateHooks(entities: ParsedEntity[], generatedDir: string) {
  const remoteHooks: Array<{
    entityType: string;
    methodName: string;
    runnerExport: string;
    sourcePackage?: string;
    pubsub?: string;
  }> = [];

  for (const entity of entities) {
    for (const hook of entity.hooks) {
      if (!hook.inProcess && hook.runnerExport) {
        remoteHooks.push({
          entityType: entity.type,
          methodName: hook.methodName,
          runnerExport: hook.runnerExport,
          sourcePackage: hook.sourcePackage,
          pubsub: entity.infra.pubsub,
        });
      }
    }
  }

  if (remoteHooks.length === 0) {
    writeFileSync(resolve(generatedDir, '_hooks.ts'), '// No remote hooks — all hooks run in-process via HookWrapper.\n');
    return;
  }

  // Collect unique pubsub adapters
  const pubsubs = new Set<string>(['InProcessBusAdapter']);
  for (const hook of remoteHooks) {
    if (hook.pubsub) pubsubs.add(hook.pubsub);
  }

  // Collect unique namespace → package imports
  // e.g. "HttpRequest.Runner(...)" → namespace "HttpRequest", package "@interactkit/http"
  const nsImports = new Map<string, string>(); // namespace → package
  for (const hook of remoteHooks) {
    const ns = hook.runnerExport.split('.')[0];
    if (hook.sourcePackage && !nsImports.has(ns)) {
      nsImports.set(ns, hook.sourcePackage);
    }
  }

  const lines = [
    '// AUTO-GENERATED by interactkit build — do not edit',
    `// Hook server: starts ${remoteHooks.length} remote hook runner(s) and publishes to pubsub.`,
    "import 'dotenv/config';",
    `import { ${[...pubsubs].join(', ')} } from '@interactkit/sdk';`,
  ];

  for (const [ns, pkg] of nsImports) {
    lines.push(`import { ${ns} } from '${pkg}';`);
  }

  lines.push('');

  // Create pubsub instances
  for (const ps of pubsubs) {
    const varName = ps.charAt(0).toLowerCase() + ps.slice(1);
    lines.push(`const ${varName} = new ${ps}();`);
  }

  lines.push('');
  lines.push('(async () => {');

  for (const hook of remoteHooks) {
    const channel = `hook:${hook.entityType}.${hook.methodName}`;
    const adapterVar = hook.pubsub
      ? hook.pubsub.charAt(0).toLowerCase() + hook.pubsub.slice(1)
      : 'inProcessBusAdapter';

    lines.push(`  {`);
    lines.push(`    const handler = ${hook.runnerExport};`);
    lines.push(`    const runner = new handler.runnerClass();`);
    lines.push(`    await runner.start(`);
    lines.push(`      async (data: unknown) => { await ${adapterVar}.publish('${channel}', data); },`);
    lines.push(`      { ...handler.config, entityId: '${hook.entityType}' },`);
    lines.push(`    );`);
    lines.push(`    console.log('▸ hook: ${hook.entityType}.${hook.methodName}');`);
    lines.push(`  }`);
  }

  lines.push('})();');
  lines.push('');

  writeFileSync(resolve(generatedDir, '_hooks.ts'), lines.join('\n'), 'utf-8');
}
